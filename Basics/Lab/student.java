package Basics.Lab;

public class Student {
    private int stdId;
    private String stdName;

    public void setStdId(int id) {
        this.stdId = id;
    }

    public void setStdName(String name) {
        this.stdName = name;
    }

    public int getStdId() {
        return stdId;
    }

    public String getStdName() {
        return stdName;
    }
}